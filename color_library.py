def export_style_xml(colors, name):
    items = ""
    for i, c in enumerate(colors):
        r, g, b = hex_to_rgb(c)
        items += (f'  <StyleItem Type="Color" Name="{name}_{i+1}">\n'
                  f'    <Color><RGB R="{r}" G="{g}" B="{b}" A="255"/></Color>\n'
                  f'  </StyleItem>\n')
    return (f'<?xml version="1.0" encoding="UTF-8"?>\n'
            f'<!-- ArcMap .style XML：样式管理器 → 加载样式 -->\n'
            f'<StyleDocument Name="{name}" Version="10.x">\n{items}</StyleDocument>')

def export_qgis_txt(colors, name):
    lines = [
        f"# QGIS Color Ramp: {name}",
        "# 使用方法：图层属性 → 符号化 → 单波段伪彩色 → 颜色渐变 → 从文件加载",
        "INTERPOLATION:INTERPOLATED",
    ]
    for i, c in enumerate(colors):
        r, g, b = hex_to_rgb(c)
        pos = 0 if len(colors) == 1 else i / (len(colors)-1)
        lines.append(f"{pos:.4f};{r};{g};{b};255;{name}_{i+1}")
    return "\n".join(lines)


def export_palette_txt(colors, name):
    """纯文本色表：无软件名，不易被误当成其它格式。"""
    lines = [
        f"# 配色名称: {name}",
        "# 每行一个 HEX（#RRGGBB），另附 RGB 对照",
        "",
    ]
    for c in colors:
        r, g, b = hex_to_rgb(c)
        lines.append(f"{c}\t{r},{g},{b}")
    return "\n".join(lines)

def export_qgis_xml(colors, name):
    stops = ""
    for i, c in enumerate(colors):
        r, g, b = hex_to_rgb(c)
        pos = 0 if len(colors) == 1 else i / (len(colors)-1)
        stops += f'      <prop k="stops" v="{pos:.4f};{r},{g},{b},255"/>\n'
    r0,g0,b0 = hex_to_rgb(colors[0])
    r1,g1,b1 = hex_to_rgb(colors[-1])
    return (f'<?xml version="1.0" encoding="UTF-8"?>\n'
            f'<!-- QGIS XML 样式：图层右键 → 样式 → 从文件加载样式 -->\n'
            f'<!DOCTYPE qgis PUBLIC \'http://mrcc.com/qgis.dtd\' \'SYSTEM\'>\n'
            f'<qgis version="3.28">\n  <colorramps>\n'
            f'    <colorramp name="{name}" type="gradient">\n'
            f'      <prop k="color1" v="{r0},{g0},{b0},255"/>\n'
            f'      <prop k="color2" v="{r1},{g1},{b1},255"/>\n'
            f'      <prop k="discrete" v="0"/>\n'
            f'      <prop k="rampType" v="gradient"/>\n'
            f'{stops}    </colorramp>\n  </colorramps>\n</qgis>')

def export_cpt(colors, name):
    n = len(colors)
    lines = [
        f"# GMT/GDAL Color Palette Table: {name}",
        "# 使用方法：gdaldem color-relief input.tif this_file.cpt output.tif",
        "COLOR_MODEL = RGB",
    ]
    for i in range(n-1):
        v0 = i * 100.0 / max(n-1, 1)
        v1 = (i+1) * 100.0 / max(n-1, 1)
        r0,g0,b0 = hex_to_rgb(colors[i])
        r1,g1,b1 = hex_to_rgb(colors[i+1])
        lines.append(f"{v0:.1f}  {r0}  {g0}  {b0}  {v1:.1f}  {r1}  {g1}  {b1}")
    if n == 1:
        r,g,b = hex_to_rgb(colors[0])
        lines.append(f"0  {r}  {g}  {b}  100  {r}  {g}  {b}")
    lines += ["B  0  0  0", "F  255  255  255", "N  128  128  128"]
    return "\n".join(lines)

def export_act(colors):
    data = bytearray(772)
    for i, c in enumerate(colors[:256]):
        r,g,b = hex_to_rgb(c)
        data[i*3]=r; data[i*3+1]=g; data[i*3+2]=b
    n = min(len(colors), 256)
    data[768]=(n>>8)&0xFF; data[769]=n&0xFF
    data[770]=0; data[771]=(n-1)&0xFF
    return bytes(data)

