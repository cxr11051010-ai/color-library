def _cim_fixed_color_ramp(colors):
    """CIM 多段色带（离散色阶），与 Esri 社区写入 Favorites 的方式一致。"""
    cim = {
        "type": "CIMFixedColorRamp",
        "arrangement": "Default",
        "colors": [],
    }
    for c in colors:
        r, g, b = hex_to_rgb(c)
        # Pro 中 RGBA 第四分量常用 100（百分比不透明）或 255；与社区示例一致用 100
        cim["colors"].append({"type": "CIMRGBColor", "values": [r, g, b, 100]})
    return cim


def _cim_rgb_color(c):
    r, g, b = hex_to_rgb(c)
    return {"type": "CIMRGBColor", "values": [r, g, b, 100]}


def _content_blob(content_dict):
    return (
        json.dumps(content_dict, ensure_ascii=False, separators=(",", ":")).encode(
            "utf-8"
        )
        + b"\x00"
    )


def _stylx_insert_item(conn, class_id, category, item_name, tags, content_dict, key):
    """
    按模板 ITEMS 表的实际列写入一行（空样式库列名/非空约束因版本而异）。
    """
    cur = conn.cursor()
    cur.execute("PRAGMA table_info(ITEMS)")
    pragma = cur.fetchall()
    if not pragma:
        raise ValueError("所选文件没有 ITEMS 表，请使用 ArcGIS Pro「新建样式」保存的 .stylx")

    blob = _content_blob(content_dict)
    col_names = [row[1] for row in pragma]

    def val_for(col):
        n = col.lower()
        if n == "id":
            return None
        if n == "class":
            return class_id
        if n == "category":
            return category or ""
        if n == "name":
            return item_name
        if n == "tags":
            return tags or "rgb"
        if n == "content":
            return blob
        if n == "key":
            return key
        if n == "physicalname":
            return key[: min(256, len(key))]
        if n in ("label", "title", "displayname"):
            return item_name
        if n in ("datemodified", "datecreated"):
            return None
        row = next((r for r in pragma if r[1] == col), None)
        if not row:
            return None
        notnull, default, col_type = row[3], row[4], (row[2] or "").upper()
        if default is not None:
            return default
        if notnull:
            if "INT" in col_type:
                return 0
            if "BLOB" in col_type or "BINARY" in col_type:
                return b""
            return ""
        return None

    insert_cols = [c for c in col_names if c.lower() != "id"]
    values = []
    for c in insert_cols:
        values.append(val_for(c))
    ph = ",".join("?" * len(insert_cols))
    qcols = ",".join(f'"{c}"' if "-" in c or " " in c else c for c in insert_cols)
    conn.execute(f"INSERT INTO ITEMS ({qcols}) VALUES ({ph})", values)


def export_stylx(colors, name, template_bytes=None):
    """
    写入 ArcGIS Pro .stylx。

    重要：仅自建 ITEMS 表时，Pro 中「样式类别」树为空、色带不显示。
    必须基于 **含 CLASSES/METADATA 等的空样式** 再写入数据。

    模板优先级：① 上传的 template_bytes ② 同目录 empty_style.stylx /
    arcgis_empty_style.stylx ③ **脚本内置** Pro 空样式（由 color.stylx 嵌入）。
    皆无时才生成极简库（Pro 中类别常为空）。
    """
    if not colors:
        raise ValueError("至少需要一个颜色才能导出 stylx")

    ramp_name = (name or "配色方案").strip() or "配色方案"
    h = hashlib.md5(
        (ramp_name + "|" + "|".join(colors)).encode("utf-8"), usedforsecurity=False
    ).hexdigest()[:10]
    key_ramp = f"{_stylx_unique_key(ramp_name, 100)}_{h}"

    tpl = template_bytes
    if tpl is None:
        for p in _STYLX_TEMPLATE_CANDIDATES:
            if os.path.isfile(p):
                with open(p, "rb") as f:
                    tpl = f.read()
                break
    if tpl is None:
        try:
            tpl = _builtin_empty_stylx_template_bytes()
        except Exception:
            tpl = None

    fd, path = tempfile.mkstemp(suffix=".stylx")
    try:
        os.close(fd)
        if tpl:
            with open(path, "wb") as out:
                out.write(tpl)
            conn = sqlite3.connect(path)
            try:
                # CLASS=2：颜色方案 / 色带（在「颜色方案」下显示）
                try:
                    _stylx_insert_item(
                        conn,
                        2,
                        "",
                        ramp_name,
                        "color_library",
                        _cim_fixed_color_ramp(colors),
                        key_ramp,
                    )
                except sqlite3.OperationalError as e:
                    raise ValueError(
                        "无法写入色带到所选 .stylx。请确认文件是在 ArcGIS Pro 中 "
                        "「插入 → 样式 → 新建样式」保存的空样式，而不是本工具以前生成的文件。"
                    ) from e
                # CLASS=1：各单色（「颜色」类别）；部分版本可省略
                for i, c in enumerate(colors):
                    try:
                        k = f"{key_ramp}_c{i+1}"
                        _stylx_insert_item(
                            conn,
                            1,
                            "",
                            f"{ramp_name}_{i+1}",
                            "color_library",
                            _cim_rgb_color(c),
                            k,
                        )
                    except sqlite3.OperationalError:
                        break
                conn.commit()
            finally:
                conn.close()
        else:
            # 无模板：旧行为（Pro 中常表现为空类别）
            conn = sqlite3.connect(path)
            try:
                conn.execute(
                    """
                    CREATE TABLE ITEMS (
                        ID INTEGER PRIMARY KEY AUTOINCREMENT,
                        CLASS INTEGER NOT NULL,
                        CATEGORY TEXT,
                        NAME TEXT,
                        TAGS TEXT,
                        CONTENT BLOB,
                        KEY TEXT NOT NULL UNIQUE
                    )
                    """
                )
                conn.execute(
                    "INSERT INTO ITEMS (CLASS, CATEGORY, NAME, TAGS, CONTENT, KEY) VALUES (?,?,?,?,?,?)",
                    (
                        2,
                        "",
                        ramp_name,
                        "rgb",
                        _content_blob(_cim_fixed_color_ramp(colors)),
                        key_ramp,
                    ),
                )
                conn.commit()
            finally:
                conn.close()

        with open(path, "rb") as f:
            return f.read()
    finally:
        try:
            os.unlink(path)
        except OSError:
            pass


