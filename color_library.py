def _safe_export_stem(name):
    """ZIP 内文件名用：避免路径非法字符。"""
    s = re.sub(r"[^\w\u4e00-\u9fff\-]", "_", (name or "palette").strip())
    s = re.sub(r"_+", "_", s).strip("_") or "palette"
    return s[:100]


def export_all_formats_zip(colors, name, stylx_template_bytes=None):
    """
    一键打包：ArcGIS Pro .clr、.stylx，通用色彩表 .txt，Python（matplotlib + geopandas 示例）.py
    """
    if not colors:
        raise ValueError("至少需要一个颜色")
    stem = _safe_export_stem(name)
    buf = BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(f"{stem}.clr", export_clr(colors, name).encode("utf-8"))
        zf.writestr(f"{stem}.txt", export_palette_txt(colors, name).encode("utf-8"))
        zf.writestr(f"{stem}.py", export_python(colors, name).encode("utf-8"))
        zf.writestr(f"{stem}.stylx", export_stylx(colors, name, template_bytes=stylx_template_bytes))
    buf.seek(0)
    return buf.getvalue()


